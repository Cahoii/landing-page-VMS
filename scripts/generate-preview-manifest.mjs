// This script scans public/preview/ for .webp files and generates
// a TypeScript file that exports the list. It runs automatically
// before `pnpm dev` and `pnpm build`.

import fs from "fs"
import path from "path"
import { fileURLToPath } from "url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, "..")
const previewDir = path.join(root, "public", "preview")
const outputFile = path.join(root, "components", "ui", "preview-images.ts")

// Ensure preview directory exists
if (!fs.existsSync(previewDir)) {
    fs.mkdirSync(previewDir, { recursive: true })
}

const files = fs
    .readdirSync(previewDir)
    .filter((f) => f.endsWith(".webp"))
    .sort()
    .map((f) => `/preview/${f}`)

const content = `// AUTO-GENERATED — do not edit manually.
// Re-generated by scripts/generate-preview-manifest.mjs
// To update, add/remove .webp files in public/preview/ and restart dev server.

export const PREVIEW_IMAGES: string[] = ${JSON.stringify(files, null, 2)}
`

fs.writeFileSync(outputFile, content, "utf-8")
console.log(`✅ Preview manifest: ${files.length} image(s) found → ${path.relative(root, outputFile)}`)
